dia = int(input('Digite um dia de 1 á 7: '))

if dia == 1 :
    print("Hoje È Domingo")

elif dia == 2 :
    print("Hoje È Segunda")

elif dia == 3 :
    print("Hoje È Terça")

elif dia == 4 :
    print("Hoje È Quarta")

elif dia == 5 :
    print("Hoje È Quinta")

elif dia == 6 :
    print("Hoje È Sexta")

elif dia == 7 :
    print("Hoje È Sabado")

else:
    print("dia não encontrado")


